//注释main102行停止粒子源
float dot(const Vec3 &a, const Vec3 &b) {
  return a.x * b.x + a.y * b.y + a.z * b.z;
}
Vec3 normalize(const Vec3 &a) {
  float len = sqrt(dot(a, a));
  return a / len;
}
float length(const Vec3 &a) { return sqrt(dot(a, a)); }
Vec3 proj(const Vec3 &a, const Vec3 &b) { return dot(a, b) / dot(b, b) * b; }
static void collision(Particle &pi,Particle & pj,float loss,float fraction){
  pi.collision_count++;
  Vec3 dir = normalize(pi.x - pj.x);
  Vec3 collision = proj(pi.v, dir);
  Vec3 new_collision = (collision+proj(pj.v, dir))*0.5f*loss+proj(pj.v, dir)*(1-loss);
  Vec3 parallel=pi.v-collision;
  Vec3 new_parallel=((parallel+(pj.v-proj(pj.v, dir))))*0.5f*fraction+parallel*(1-fraction);
  pi.v=new_parallel+new_collision;
  pi.v*=0.8f;
}
void ParticleSystem::basic_sph_solver() {
  buildGrid();
  for (auto &pi : particles) {
    pi.density = getDensity(pi.x);
    //printf("%f %f %f ;\n",pi.x.x,pi.x.y,pi.x.z);
    pi.collision_count=0;
    pi.pressure = GasK * (pi.density - density_0);
  }

  for (auto &pi : particles) {
    Vec3 a_pressure{0, 0, 0};
    Vec3 a_viscosity{0, 0, 0};

    IVec3 index = coordToGridIndex(pi.x);
    for (int i = -1; i <= 1; i++) {
      for (int j = -1; j <= 1; j++) {
        for (int k = -1; k <= 1; k++) {
          IVec3 neighbor_index = index + IVec3(i, j, k);
          long long hash = gridIndexToHash(neighbor_index);
          if (grid.find(hash) == grid.end())
            continue;
          for (auto &pj_ptr : grid[hash]) {
            Particle &pj = *pj_ptr;
            // for (auto &pj : particles) {
            Vec3 r = pi.x - pj.x;
            Float r_len = glm::length(r);
            if (r_len > H || r_len < epsilon)
              continue;
            if(r_len<1.8f*particle_radius){
              a_pressure += 0.00001f*((float)pow(2,(1.8f*particle_radius - r_len)/1.8f*particle_radius)) * r;
              //pi.v*=0.9f;
              collision(pi,pj,0.1f,0.1f);
              //pi.v*=0.99f;
            }
            if(r_len<particle_radius){
              //pi.v*=0.5f;
              //collision(pi.v,pj.v,0.1f);
                  int indicator=rand()%3;
                  pi.a+=Vec3{0,0.9f,0}*(float)(indicator-1);
                  pj.a-=Vec3{0,0.9f,0}*(float)(indicator-1);
                  pi.v*=0.5f;
            }
            a_pressure += Float((pi.pressure + pj.pressure) /
                                (2 * pi.density * pj.density) *
                                pow(H - r_len, 2) / r_len) *
                          r;
            a_viscosity +=
                Float((H - r_len) / (pi.density * pj.density)) * (pj.v - pi.v);
          }
        }
      }
    }

    a_pressure =
        particle_mass * Float(45) / Float(M_PI * pow(H, 6)) * a_pressure;
    a_viscosity = particle_mass * viscosity * Float(45) /
                  Float(M_PI * pow(H, 6)) * a_viscosity;
    auto a = g + a_pressure + a_viscosity;
    pi.v += a * fixed_delta_time;
    pi.x += pi.v * fixed_delta_time;
    if (pi.x.x < xmin)
      pi.x.x = xmin, pi.v.x *= -.3;
    if (pi.x.x > xmax)
      pi.x.x = xmax, pi.v.x *= -.3;
    if (pi.x.y < ymin)
      pi.x.y = ymin, pi.v.y *= -.3;
    if (pi.x.y > ymax)
      pi.x.y = ymax, pi.v.y *= -.3;
    if (pi.x.z < zmin)
      pi.x.z = zmin, pi.v.z *= -.3;
    if (pi.x.z > zmax)
      pi.x.z = zmax, pi.v.z *= -.3;
  }
}

void ParticleSystem::buildGrid() {
  grid.clear();
  for (auto &p : particles) {
    auto index = coordToGridIndex(p.x);
    auto hash = gridIndexToHash(index);
    grid[hash].push_back(&p);
  }
  for (auto &pi : particles) {
    pi.neighbors.clear();
    IVec3 index = coordToGridIndex(pi.x);
    for (int i = std::max(-1, 0 - index.x);
         i <= std::min<int>(1, grid_size_x - index.x); i++) {
      for (int j = std::max(-1, 0 - index.y);
           j <= std::min<int>(1, grid_size_y - index.y); j++) {
        for (int k = std::max(-1, 0 - index.z);
             k <= std::min<int>(1, grid_size_z - index.z); k++) {
          IVec3 neighbor_index = index + IVec3(i, j, k);
          long long hash = gridIndexToHash(neighbor_index);
          if (grid.find(hash) == grid.end())
            continue;
          for (auto &pj_ptr : grid[hash]) {
            if (&pi == pj_ptr)
              continue;
            Particle &pj = *pj_ptr;
            Vec3 r = pi.x - pj.x;
            Float r_len = glm::length(r);
            if (r_len > H)
              continue;
            pi.neighbors.push_back(&pj);
          }
        }
      }
    }
  }
}
IVec3 ParticleSystem::coordToGridIndex(const Vec3 &x) {
  auto ret = IVec3((x.x - xmin) / H, (x.y - ymin) / H, (x.z - zmin) / H);
  ret.x = std::clamp<int>(ret.x, 0, grid_size_x - 1);
  ret.y = std::clamp<int>(ret.y, 0, grid_size_y - 1);
  ret.z = std::clamp<int>(ret.z, 0, grid_size_z - 1);
  return ret;
}
long long ParticleSystem::gridIndexToHash(const IVec3 &index) {
  return index.x * grid_size_y * grid_size_z + index.y * grid_size_z + index.z;
}

